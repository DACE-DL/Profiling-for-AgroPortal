package agroPortalProfiling.util;

public class UriAndString {
	
	private String uri;
	private String str;
	
	public UriAndString() {
	}
	public UriAndString(String uri, String str) {
		this.uri = uri;
		this.str = str;
	}
	public String getUri() {
		return uri;
	}
	public void setUri(String uri) {
		this.uri = uri;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
}
